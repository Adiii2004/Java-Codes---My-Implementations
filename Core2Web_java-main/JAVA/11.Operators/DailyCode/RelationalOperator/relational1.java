/*
Relational Operators
1)==
2)!=
3)<
4)>
5)<=
6)>=
**/


class Relational1 {

	public static void main (String[] args) {

		int x = 8;

		int y = 9;

		System.out.print(x == y);  //Output get False
		System.out.print(x <= y);//true
		System.out.print(x >= y);//false
		System.out.print(x < y);//true
		System.out.print(x > y);//false
	}

}
