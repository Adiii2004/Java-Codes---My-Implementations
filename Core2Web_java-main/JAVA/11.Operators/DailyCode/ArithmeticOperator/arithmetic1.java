class Arithmetic1 {

	public static void main (String[] args) {

		int x = 10;
		int y = 5;

		System.out.println(x+y);//Addition =  15
		System.out.println(x-y);//Substraction = 5
		System.out.println(x*y);//multiplication = 50
		System.out.println(x/y);//mod returns the reminder = 2
		System.out.println(x%y);//Division returns the quotient = 0 		


	}

}
